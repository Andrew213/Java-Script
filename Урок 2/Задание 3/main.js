let summa = 0;

while (true) {
   let n = prompt('Enter the number');
   if (n === '') {
      alert('Пустая строка');
      continue;
   } else if (n === null) {
      break;
   } else if (isNaN(parseFloat(n)) || !isFinite(n)) {
      alert('Это не число или строка пустая')
      continue;
   }
   summa += parseFloat(n)
}
